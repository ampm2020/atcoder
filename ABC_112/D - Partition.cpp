//約数列挙
vector<long long> enum_divisors(long long N) {
    vector<long long> res;
    for (long long i = 1; i * i <= N; ++i) {
        if (N % i == 0) {
            res.push_back(i);
            // 重複しないならば i の相方である N/i も push
            if (N/i != i) res.push_back(N/i);
        }
    }
    // 小さい順に並び替える
    sort(res.begin(), res.end());
    return res;
}

int main() {
    int n; ll m; cin >> n >> m;
    vector<ll> yakusu = enum_divisors(m);
    sort(all(yakusu));
    ll mx = m / n, res;
    for(ll num: yakusu){
        if(num <= mx) res = num;
        else break;
    }
    cout << res << ln;
}