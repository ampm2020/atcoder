vector<pair<long long, long long> > prime_factorize(long long N) {
    vector<pair<long long, long long> > res;
    for (long long a = 2; a * a <= N; ++a) {
        if (N % a != 0) continue;
        long long ex = 0; // 指数

        while (N % a == 0) {
            ++ex;
            N /= a;
        }
        res.push_back({a, ex});
    }
    if (N != 1) res.push_back({N, 1});
    return res;
}

int main() {
    ll n, p; cin >> n >> p;
    vector<pll> div = prime_factorize(p);
    ll res = 1;
    for(auto d: div){
        int num = d.second;
        while(num >= n){
            res *= d.first;
            num -= n;
        }
    }
    cout << res << ln;
}
